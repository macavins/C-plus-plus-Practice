#include <iostream>

using namespace std;

int main() 
{
    double num1, num2;
    cout << "Enter first number: ";
    cin >> num1;

    cout << "Enter second number: ";
    cin >> num2;

    cout << "add: " << num1 + num2 << endl;
    cout << "sub: " << num1 - num2 << endl;
    cout << "mul: " << num1 * num2 << endl;
    cout << "div: " << num1 / num2 << endl;


    return 0;
}